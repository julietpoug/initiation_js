/* 
INCREMENTATION & DECREMENTATION
*/

// Incrementation

var nb1 = 1;
nb1 = nb1 + 1;
nb1 += 1;

// écriture simplifiée

nb1++;
console.log(nb1);

// décrémentation
var nb2 = 1;
nb2 = nb1 - 1;
nb2 -= 1;

// écriture simplifiée

nb2--;
console.log(nb1);

// subtilité

nb1++; /* à le même résultat que  */ ++nb1;
// cependant nb1--; est égal à --nb1



